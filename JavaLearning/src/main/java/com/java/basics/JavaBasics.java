package com.java.basics;

public class JavaBasics {

	public static void main(String[] args) {
		
		// Variables, Data types
		int x = 10;
		
		char c = 65;
		
		char a = 's';
		
		byte b = 12;
		
		short s = 3444;
		
		long l = 239089028;
		
		double d = 8937893.2398423;
		
		float f = 38298f;
		
		boolean bool = true;
		
		String str = "Sudarshan";
		
		
		System.out.println("int type: " + x);
		System.out.println("char type: " + a);
		System.out.println("char ASCII type: " + c);
		System.out.println("byte type: " + b);
		System.out.println("short type: " + s);
		System.out.println("long type: " + l);
		System.out.println("double type: " + d);
		System.out.println("float type: " + f);
		System.out.println("boolean type: " + bool);
		System.out.println("String type: " + str);
		
	}
}
